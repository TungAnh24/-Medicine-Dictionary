    public void displayVie() {
        vietListDTO = vietController.displayVies();
        if (vietListDTO != null) {
            defaultTabVie.setRowCount(0);
            vietListDTO.stream().forEach(v -> {
                defaultTabVie.addRow(new Object[]{
                    v.getId(),
                    v.getWordVie()
                });
            });
        }
    }

    public void displayEng() {
        engListDTO = engController.displayEngs();
        if (engListDTO != null) {
            defaultTabEng.setRowCount(0);
            engListDTO.stream().forEach(e -> {
                defaultTabEng.addRow(new Object[]{
                    e.getId(),
                    e.getWordEng()
                });
            });
        }
    }

