    private void btnAccesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccesActionPerformed
        // TODO add your handling code here:
        int sltRowVie = tblWordViet.getSelectedRow();
        int sltRowEng = tblWordEng.getSelectedRow();
        try {
            var res = JOptionPane.showConfirmDialog(null, "Bạn muốn định nghĩa thuật ngữ?", "Thông báo", JOptionPane.YES_NO_OPTION);
            if (res == JOptionPane.YES_OPTION) {
                boolean check = transController.addTrans(tblWordViet.getValueAt(sltRowVie, 0).toString(), tblWordEng.getValueAt(sltRowEng, 0).toString());
                if (check) {
                    JOptionPane.showConfirmDialog(null, "Cặp từ điển '" + txtWordVie.getText() + "' - '" + txtWordEng.getText() + "' " + " định nghĩa thành công.", "Cảnh báo", JOptionPane.OK_CANCEL_OPTION);
                }
                System.out.println(tblWordViet.getValueAt(sltRowVie, 0).toString() + tblWordEng.getValueAt(sltRowEng, 0).toString());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Cặp từ điển '" + txtWordVie.getText() + "' - '" + txtWordEng.getText() + "' " + " đã tồn tại.", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAccesActionPerformed

